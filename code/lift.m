function out = lift(n,u,v,p)
%把n提升到2n的网格上
if nargin < 4
    disp("错误输入，请输入n,u,v,p")
    return 
end

%% 初始化
lv = zeros(2*n,2*n+1);
lu = zeros(2*n+1,2*n);
lp = zeros(2*n);

%% 插值

lv(1,1:2:(2*n+1)) = v(1,:)/2;
lv(2:2:(2*n-2),1:2:(2*n+1)) = v(1:n-1,1:n+1)*0.75+v(2:n,1:n+1)*0.25;
lv(3:2:(2*n-1),1:2:(2*n+1)) = v(1:n-1,1:n+1)*0.25+v(2:n,1:n+1)*0.75;
lv(2*n,1:2:(2*n+1)) = v(n,:)/2;

lv(:,2:2:2*n) = lv(:,1:2:2*n-1)/2+lv(:,3:2:2*n+1)/2;

lu(1:2:(2*n+1)) = u(:,1)/2;
lu(1:2:2*n+1,2:2:2*n-2) = u(1:n+1,1:n-1)*0.75+u(1:n+1,2:n)*0.25;
lu(1:2:2*n+1,3:2:2*n-1) = u(1:n+1,1:n-1)*0.25+u(1:n+1,2:n)*0.75;
lu(1:2:2*n+1,2*n) = u(:,n)/2;

lu(2:2:2*n,:) = lu(1:2:2*n-1,:)/2+lu(3:2:2*n+1,:)/2;

lp(2:2:2*n,2:2:n*2) = p;
lp(1:2:2*n-1,1:2:2*n-1) = p;
lp(1:2:2*n-1,2:2:n*2) = p;
lp(2:2:2*n,1:2:2*n-1) = p;

%% 输出
out.p = lp;
out.v = lv;
out.u = lu;